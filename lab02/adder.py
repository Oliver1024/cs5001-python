def main():
    # Input the numbers and only integer numbers are accepted
    number1 = input("Enter a first value: ")
    number2 = input("Enter a second value: ")

    # Calculate the sum of number1 and number2 
    total_sum = int(number1) + int(number2)

    # Print the result
    print("The sum of", number1, "and", number2, "is", total_sum)


main()
